        Citizen.Wait(rotationTimeout)
    end
end)

-- Main thread for updating activity information
Citizen.CreateThread(function()
    local wait = 1000 * (CONFIG.Option.Update.Time - 2)
    
    while CONFIG.Option.Display.Activity.Enable do
        local ped = PlayerPedId()
        local coords = GetEntityCoords(ped, true)
        local zoneName = GetNameOfZone(coords.x, coords.y, coords.z)
        local zone = CONFIG.Zone[zoneName] or zoneName
        local state = getPlayerActivities(ped, zone)

        currentActivity = state

        Citizen.Wait(wait)
    end
end)

-- Main thread for updating Discord Rich Presence
Citizen.CreateThread(function()
    Citizen.Wait(1000) -- Initial wait to ensure everything is loaded
    
    -- Get player data
    updatePlayerData()
    
    -- Set up Rich Presence
    setRichPresence()
    
    -- Wait for the state from the server
    Citizen.Wait(2000)
    
    while true do
        local state = GlobalState[resource]
        
        -- Format player ID and name
        local id = (CONFIG.Option.Display.ID.Enable and string.format('[ID: %s] - ', GetPlayerServerId(PlayerId())) or '')
        local name = ""
        
        if CONFIG.Option.Display.Name.Enable then
            if CONFIG.Option.Display.Name.UseCharacterName and PlayerData.charinfo then
                name = string.format('%s %s', PlayerData.charinfo.firstname, PlayerData.charinfo.lastname)
            else
                name = GetPlayerName(PlayerId())
            end
        end
        
        -- Format online player count
        local online = (state and (CONFIG.Option.Display.Online.Enable and 
            string.format('#NCHub V3.0 | [%s/%s %s]', 
                formatInt(state.players), 
                formatInt(state.maxclients), 
                CONFIG.Translate.General.Player) or 
            string.format('%s %s', 
                formatInt(state.maxclients), 
                CONFIG.Translate.General.Slot)) or 
            CONFIG.Translate.General.Loading)
        
        -- Build the rich presence detail string
        local displayInfo = ""
        
        if CONFIG.Option.Display.Rotation.Enable then
            displayInfo = currentRotationData
        elseif CONFIG.Option.Display.Job.Enable then
            displayInfo = getPlayerJob()
        elseif CONFIG.Option.Display.Activity.Enable then
            displayInfo = currentActivity
        end
        
        -- Only show the dash if we have info to display
        local separator = (displayInfo ~= "" and " - " or "")
        
        -- Build the final rich presence text
        local detail = string.format('%s%s%s%s\n%s', 
            id, 
            name, 
            separator,
            displayInfo, 
            online)
        
        -- Set the Rich Presence
        SetRichPresence(detail)
        
        -- Update dynamic icons
        setRichPresence()
        
        -- Wait for next update
        Citizen.Wait(timeout)
    end
end)

