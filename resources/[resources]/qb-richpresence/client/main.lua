-- Get player's job information
local function getPlayerJob()
    if not PlayerData.job then return CONFIG.Translate.Unknown.Job end
    
    if CONFIG.Option.Display.Job.ShowGrade and PlayerData.job.grade and PlayerData.job.grade.name then
        return string.format(CONFIG.Translate.Job.Format, PlayerData.job.label or PlayerData.job.name, PlayerData.job.grade.name)
    else
        return string.format(CONFIG.Translate.Job.FormatNoGrade, PlayerData.job.label or PlayerData.job.name)
    end
end

-- Get player's money information
local function getPlayerMoney()
    if not PlayerData.money then return "" end
    
    if CONFIG.Option.Display.Money.ShowBank and CONFIG.Option.Display.Money.ShowCash then
        return string.format(CONFIG.Translate.Money.Format, 
            formatMoney(PlayerData.money.bank), 
            formatMoney(PlayerData.money.cash))
    elseif CONFIG.Option.Display.Money.ShowBank then
        return string.format(CONFIG.Translate.Money.FormatBankOnly, formatMoney(PlayerData.money.bank))
    elseif CONFIG.Option.Display.Money.ShowCash then
        return string.format(CONFIG.Translate.Money.FormatCashOnly, formatMoney(PlayerData.money.cash))
    else
        return ""
    end
end

-- Get player's activities based on what they're doing
local function getPlayerActivities(ped, zone)
    local zone = zone or CONFIG.Translate.Unknown.Zone
    local dead = IsEntityDead(ped)
    local water = IsEntityInWater(ped)

    if IsPedOnFoot(ped) and not water then
        if dead then
            return string.format(CONFIG.Translate.Activity.Dead.Ground, zone)
        elseif IsPedStill(ped) then
            return string.format(CONFIG.Translate.Activity.Ground.Standing, zone)
        elseif IsPedWalking(ped) then
            return string.format(CONFIG.Translate.Activity.Ground.Walking, zone)
        elseif IsPedRunning(ped) then
            return string.format(CONFIG.Translate.Activity.Ground.Running, zone)
        elseif IsPedSprinting(ped) then
            return string.format(CONFIG.Translate.Activity.Ground.Sprinting, zone)
        end
    elseif water then
        if dead then
            return string.format(CONFIG.Translate.Activity.Dead.Water, zone)
        else
            return string.format(CONFIG.Translate.Activity.Water.Swimming, zone)
        end
    end

    local vehicle = GetVehiclePedIsIn(ped, false)

    if vehicle ~= 0 then
        local label = GetLabelText(GetDisplayNameFromVehicleModel(GetEntityModel(vehicle)))
        if label == "NULL" then
            label = GetDisplayNameFromVehicleModel(GetEntityModel(vehicle))
        end
        local plate = (CONFIG.Option.Display.Activity.Plate.Enable and string.format('(%s)', GetVehicleNumberPlateText(vehicle)) or '')

        if dead then
            return string.format(CONFIG.Translate.Activity.Dead.Vehicle, zone, label, plate)
        elseif IsPedInAnyBoat(ped) or IsPedInAnySub(ped) then
            return string.format(CONFIG.Translate.Activity.Vehicle.Sailing, zone, label, plate)
        elseif IsEntityInWater(vehicle) then
            return string.format(CONFIG.Translate.Activity.Vehicle.Drowning, zone, label, plate)
        elseif IsPedInAnyHeli(ped) or IsPedInAnyPlane(ped) then
            if IsEntityInAir(vehicle) then
                return string.format(CONFIG.Translate.Activity.Vehicle.Flying, zone, label, plate)
            else
                return string.format(CONFIG.Translate.Activity.Vehicle.Landed, zone, label, plate)
            end
        else
            local meters = GetEntitySpeed(vehicle)
            local speed = math.ceil(meters * 3.6)   -- KMH = * 3.6 | MPH = * 2.236936

            if speed == 0 then
                return string.format(CONFIG.Translate.Activity.Vehicle.Parked, zone, label, plate)
            elseif speed <= 60 then -- KMH = 60 | MPH = 50
                return string.format(CONFIG.Translate.Activity.Vehicle.Cruising, zone, label, plate)
            else
                return string.format(CONFIG.Translate.Activity.Vehicle.Speeding, zone, label, plate)
            end
        end
    end

    if dead then
        return string.format(CONFIG.Translate.Activity.Dead.Ground, zone)
    end

    return CONFIG.Translate.Unknown.Activity
end

-- Update player data from QBCore
local function updatePlayerData()
    local Player = QBCore.Functions.GetPlayerData()
    if Player then
        PlayerData = Player
    end
end

-- Handle information rotation
local function rotateInformation()
    if not CONFIG.Option.Display.Rotation.Enable or #CONFIG.Option.Display.Rotation.Types == 0 then
        return ""
    end
    
    -- Get the current rotation type
    local rotationType = CONFIG.Option.Display.Rotation.Types[currentRotationIndex]
    
    -- Move to the next rotation index for next time
    currentRotationIndex = currentRotationIndex + 1
    if currentRotationIndex > #CONFIG.Option.Display.Rotation.Types then
        currentRotationIndex = 1
    end
    
    -- Return the appropriate information based on the current rotation type
    if rotationType == "job" and CONFIG.Option.Display.Job.Enable then
        return getPlayerJob()
    elseif rotationType == "activity" and CONFIG.Option.Display.Activity.Enable then
        return currentActivity
    elseif rotationType == "money" and CONFIG.Option.Display.Money.Enable then
        return getPlayerMoney()
    end
    
    return ""
end

